package com.dicoding.justbuyit;

public class Stuff {
    private String name;
    private String price;
    private String shop;
    private String rating;
    private String tx_success;
    private String whislist;
    private String photo;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getShop() {
        return shop;
    }

    public void setShop(String shop) {
        this.shop = shop;
    }

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    public String getTx_success() {
        return tx_success;
    }

    public void setTx_success(String tx_success) {
        this.tx_success = tx_success;
    }

    public String getWhislist() {
        return whislist;
    }

    public void setWhislist(String whislist) {
        this.whislist = whislist;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public String getSpec() {
        return spec;
    }

    public void setSpec(String spec) {
        this.spec = spec;
    }

    private String spec;
}
